
public class Coordinate2D {
	
	double x = 0;
	double y = 0;
	
	public Coordinate2D(){}
	
	public Coordinate2D(double x, double y){
		this.x = x;
		this.y = y;
	}
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
}
